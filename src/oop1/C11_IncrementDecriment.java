package oop1;

public class C11_IncrementDecriment {
    public static void main(String[] args) {
        int sayi = 24;

        // sayinin değerini artıralım

        sayi = sayi + 5;

        System.out.println(sayi);

        sayi += 5;

        System.out.println(sayi);

        sayi++;

        System.out.println(sayi);

        double dbl = 3.5;

        dbl = dbl * 3;

        System.out.println(dbl);

        dbl *= 3;

        System.out.println("Yeni deger dbl= " + dbl);

    }
}
